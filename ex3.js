// 3. Crie um programa que peça um número e verifique se ele é positivo,
// negativo ou zero.

let numero = Number(prompt('digite um número:'));

if(numero > 0) {
    document.write("esse número é positivo")   
}else if (numero < 0){
    document.write("esse número é negativo")
}else {
    document.write("esse número é zero")
}


